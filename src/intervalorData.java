
public class intervalorData {
	private Funcionario funcionario;
	private String quantasAtividadeFunc; 
	private Tarefas tarefa;
	
	public Funcionario getFuncionario() {
		return funcionario;
	}
	public void setFuncionario(Funcionario funcionario) {
		this.funcionario = funcionario;
	}
	public String getQuantasAtividadeFunc() {
		return quantasAtividadeFunc;
	}
	public void setQuantasAtividadeFunc(String quantasAtividadeFunc) {
		this.quantasAtividadeFunc = quantasAtividadeFunc;
	}
	public Tarefas getTarefa() {
		return tarefa;
	}
	public void setTarefa(Tarefas tarefa) {
		this.tarefa = tarefa;
	}
	
	
}
